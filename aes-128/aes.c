uint8_t *rotword_sub_rcon(uint8_t *p, int roundNumb)
{
    static uint8_t arr[4];
    arr[0] = *(p + 13);
    arr[1] = *(p + 14);
    arr[2] = *(p + 15);
    arr[3] = *(p + 12);
    for(int i=0; i<4; i++)
    {
        arr[i] = sbox[arr[i]];
    }

    arr[0] ^= rcon[roundNumb-1];
    return arr;
}

uint8_t *roundKey(uint8_t *p, int roundNumb)
{
    static uint8_t tmp[16];
    uint8_t *px;
    px = rotword_sub_rcon(p, roundNumb);
    for(int i=0; i<4; i++)
    {
        tmp[i] = *(px + i) ^ *(p + i);
    } 

    for(int i=4; i<16; i++)
    {
        tmp[i] = tmp[i - 4] ^ *(p + i);
    }
    return tmp;
}

