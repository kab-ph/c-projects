const uint8_t rcon[10] = {0x1, 0x2, 0x4, 0x8, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36};

uint8_t roundkeys[11][16];

void subBytes(uint8_t *p)
{
    /*Sbox function*/
    for(int i=0; i<16; i++)
    {
        *(p + i) = sbox[*(p + i)];          
    }
}


uint8_t mult_2(uint8_t value)
{
    if((value&128) == 128 )
    {
        return (value << 1) ^ 0x1b;
    }
    else
    {
        return value << 1;
    }
}

uint8_t mult_3(uint8_t value)
{
    return mult_2(value) ^ value;
}


void mixcolumn_colum(uint8_t *p)
{
    uint8_t tmp[4];
    for (int i=0; i<4; i++)
    {
        tmp[i] = *(p+i);
    }
    *p = mult_2(tmp[0]) ^ mult_3(tmp[1]) ^ tmp[2] ^ tmp[3];
    *(p + 1) = tmp[0] ^ mult_2(tmp[1]) ^ mult_3(tmp[2]) ^ tmp[3];
    *(p + 2) = tmp[0] ^ tmp[1] ^ mult_2(tmp[2]) ^ mult_3(tmp[3]);
    *(p + 3) = mult_3(tmp[0]) ^ tmp[1] ^ tmp[2] ^ mult_2(tmp[3]);
}

void mixcolumn(uint8_t *p)
{
    mixcolumn_colum(p);
    mixcolumn_colum(p + 4);
    mixcolumn_colum(p + 8);
    mixcolumn_colum(p + 12);
}


